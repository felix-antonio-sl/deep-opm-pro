    agregarASeleccion,
    toggleSeleccion,
    vaciarSeleccion,
    redimensionarAparienciaEnCanvas,
    reanclarExtremoAccion,
    renombrarEntidadDesdeOpl,
    gridConfig,
    solicitudFitToken,
